-- Задание 15:
-- Из таблицы user_actions получите информацию о всех отменах заказов, 
-- которые пользователи совершали в течение августа 2022 года по средам с 12:00 до 15:59:59.
-- Результат отсортируйте по убыванию id отменённых заказов.
-- Поля в результирующей таблице: user_id, order_id, action, time
SELECT user_id, order_id, action, time
FROM user_actions
WHERE action = 'cancel_order' AND 
DATE_PART('dow', time) = 3 AND 
DATE_PART('hour', time) >= 12 AND 
DATE_PART('hour', time) < 16
AND time BETWEEN '2022-08-01' AND '2022-09-01'
ORDER BY order_id DESC
-- Задание 16:
-- Как и в задаче из прошлого урока, вычислите НДС каждого товара в таблице products
-- и рассчитайте цену без учёта НДС. Однако теперь примите во внимание, что для товаров
-- из списка налог составляет 10%. Для остальных товаров НДС тот же — 20%.
-- Выведите всю информацию о товарах, включая сумму налога и цену без его учёта. 
-- Колонки с суммой налога и ценой без НДС назовите соответственно tax и price_before_tax.
-- Округлите значения в этих колонках до двух знаков после запятой.
